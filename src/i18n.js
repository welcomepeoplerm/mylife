class I18n {
  constructor() {
    this.currentLang = this.getStoredLanguage() || this.detectLanguage();
    this.listeners = [];
  }
  
  // Rileva la lingua del browser
  detectLanguage() {
    const browserLang = navigator.language.split('-')[0];
    return translations[browserLang] ? browserLang : 'it';
  }
  
  // Recupera la lingua salvata
  getStoredLanguage() {
    return localStorage.getItem('mylyfe-lang');
  }
  
  // Imposta la lingua
  setLanguage(lang) {
    if (!translations[lang]) {
      console.warn(`Language ${lang} not supported, falling back to Italian`);
      lang = 'it';
    }
    
    this.currentLang = lang;
    localStorage.setItem('mylyfe-lang', lang);
    document.documentElement.lang = lang;
    
    // Notifica i listener del cambio lingua
    this.listeners.forEach(callback => callback(lang));
  }
  
  // Ottiene una traduzione
  t(key, fallback = '') {
    const value = translations[this.currentLang]?.[key];
    if (value !== undefined) return value;
    
    // Fallback su italiano
    const italianValue = translations.it?.[key];
    if (italianValue !== undefined) return italianValue;
    
    return fallback || key;
  }
  
  // Ottiene traduzione da oggetto multilingua (es: {it: "...", en: "..."})
  tm(obj) {
    if (!obj) return '';
    if (typeof obj === 'string') return obj;
    return obj[this.currentLang] || obj.it || Object.values(obj)[0] || '';
  }
  
  // Registra un listener per cambio lingua
  onChange(callback) {
    this.listeners.push(callback);
    return () => {
      this.listeners = this.listeners.filter(cb => cb !== callback);
    };
  }
  
  // Ottiene la lingua corrente
  getCurrentLanguage() {
    return this.currentLang;
  }
  
  // Ottiene tutte le lingue disponibili
  getAvailableLanguages() {
    return [
      { code: 'it', name: 'Italiano', flag: '🇮🇹' },
      { code: 'en', name: 'English', flag: '🇬🇧' },
      { code: 'fr', name: 'Français', flag: '🇫🇷' },
      { code: 'de', name: 'Deutsch', flag: '🇩🇪' },
      { code: 'es', name: 'Español', flag: '🇪🇸' }
    ];
  }
}

// Istanza singleton
export const i18n = new I18n();
export default i18n;
